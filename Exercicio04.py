# Tipos de prints com Python

# Primeiro print
print('Hello, Word!')
# Print com variável nome
nome = 'Helo, Word!'
print(nome)
# Print str + variável nome
print('Quebrando a maldição, ' + nome)
# Print com f'string
print(f'Quebrando a maldição {nome}')
# Print com .format
print('Quebrei a maldição {}'.format(nome))